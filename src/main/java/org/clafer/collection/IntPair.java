package org.clafer.collection;

/**
 *
 * @author jimmy
 */
public class IntPair {

    private final int fst, snd;

    public IntPair(int fst, int snd) {
        this.fst = fst;
        this.snd = snd;
    }

    public int getFst() {
        return fst;
    }

    public int getSnd() {
        return snd;
    }
}
